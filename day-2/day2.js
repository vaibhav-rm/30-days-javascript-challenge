// Task 1: Addition
let num1 = 10;
let num2 = 5;
let sum = num1 + num2;
console.log(`Task 1: Sum of ${num1} and ${num2} is ${sum}`);

// Task 2: Subtraction
let difference = num1 - num2;
console.log(`Task 2: Difference between ${num1} and ${num2} is ${difference}`);

// Task 3: Multiplication
let product = num1 * num2;
console.log(`Task 3: Product of ${num1} and ${num2} is ${product}`);

// Task 4: Division
let quotient = num1 / num2;
console.log(`Task 4: Quotient of ${num1} divided by ${num2} is ${quotient}`);

// Task 5: Remainder
let remainder = num1 % num2;
console.log(`Task 5: Remainder when ${num1} is divided by ${num2} is ${remainder}`);

// Task 6: Assignment - Adding a number to a variable
let number = 10;
let addToNumber = 5;
number += addToNumber;
console.log(`Task 6: Added ${addToNumber} to number, result is ${number}`);

// Task 7: Assignment - Subtracting a number from a variable
let subtractFromNumber = 3;
number -= subtractFromNumber;
console.log(`Task 7: Subtracted ${subtractFromNumber} from number, result is ${number}`);

// Task 8: Comparison using >
let compareNum1 = 10;
let compareNum2 = 5;
let greaterThanResult = compareNum1 > compareNum2;
console.log(`Task 8: ${compareNum1} > ${compareNum2} is ${greaterThanResult}`);

// Task 9: Comparison using <
let lessThanResult = compareNum1 < compareNum2;
console.log(`Task 9: ${compareNum1} < ${compareNum2} is ${lessThanResult}`);

// Task 10: Comparison using ===
let isEqual = compareNum1 === compareNum2;
console.log(`Task 10: ${compareNum1} === ${compareNum2} is ${isEqual}`);

// Task 11: Logical AND (&&)
let condition1 = true;
let condition2 = false;
let andResult = condition1 && condition2;
console.log(`Task 11: ${condition1} && ${condition2} is ${andResult}`);

// Task 12: Logical OR (||)
let orResult = condition1 || condition2;
console.log(`Task 12: ${condition1} || ${condition2} is ${orResult}`);

// Task 13: Logical NOT (!)
let notResult = !condition1;
console.log(`Task 13: !${condition1} is ${notResult}`);

// Task 14: Ternary Operator
let numberToCheck = -5;
let result = (numberToCheck >= 0) ? "Positive" : "Negative";
console.log(`Task 14: Number ${numberToCheck} is ${result}`);
